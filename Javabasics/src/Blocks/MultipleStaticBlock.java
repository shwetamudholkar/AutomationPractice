package Blocks;

public class MultipleStaticBlock {

	
   static {
	System.out.println("#######MULTIPLE STATIC BLOCKS STARTS########");
}
   public static void main(String args[]) {
	
System.out.println("We start the Multiple Static Blocks");
System.out.println("The multiple static blocks are very useful in JAVA");
System.out.println("Here is end of Multiple Static Blocks");
	}
	static {
		System.out.println("********* MULTIPLE STATIC BLOCKS ENDS***********");
	}

}

